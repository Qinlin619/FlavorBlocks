// 增加轮播图拖拽滚动功能
const carousel = document.querySelector('.flavor-carousel');
if (carousel) {
    let isDown = false;
    let startX;
    let scrollLeft;
    let startY;
    let scrollTop;

    carousel.addEventListener('mousedown', (e) => {
        isDown = true;
        carousel.classList.add('active');
        startX = e.pageX - carousel.offsetLeft;
        startY = e.pageY - carousel.offsetTop;
        scrollLeft = carousel.scrollLeft;
        scrollTop = carousel.scrollTop;
        carousel.style.cursor = 'grabbing';
    });

    carousel.addEventListener('mouseleave', () => {
        isDown = false;
        carousel.style.cursor = 'grab';
    });

    carousel.addEventListener('mouseup', () => {
        isDown = false;
        carousel.style.cursor = 'grab';
    });

    carousel.addEventListener('mousemove', (e) => {
        if (!isDown) return;
        e.preventDefault();
        const x = e.pageX - carousel.offsetLeft;
        const y = e.pageY - carousel.offsetTop;
        const walkX = (x - startX) * 2;
        const walkY = (y - startY) * 2;
        carousel.scrollLeft = scrollLeft - walkX;
        carousel.scrollTop = scrollTop - walkY;
    });

    // 为移动设备触摸增加支持
    carousel.addEventListener('touchstart', (e) => {
        isDown = true;
        carousel.classList.add('active');
        const touch = e.touches[0];
        startX = touch.pageX - carousel.offsetLeft;
        startY = touch.pageY - carousel.offsetTop;
        scrollLeft = carousel.scrollLeft;
        scrollTop = carousel.scrollTop;
    }, { passive: true });

    carousel.addEventListener('touchend', () => {
        isDown = false;
    });

    carousel.addEventListener('touchcancel', () => {
        isDown = false;
    });

    carousel.addEventListener('touchmove', (e) => {
        if (!isDown) return;
        if (window.innerWidth <= 768) {
            // 对手机端，原生滚动其实已经由 overflow-y 特性支持，
            // 但如果因为某种环境拦截了，我们可以主动根据 touch 赋滚动值。
            // 不进行 preventDefault()，保证自然滑动感受。
            const touch = e.touches[0];
            const y = touch.pageY - carousel.offsetTop;
            const walkY = (y - startY) * 1.5;
            carousel.scrollTop = scrollTop - walkY;
        } else {
            // 电脑横向触摸或兼容横向
            const touch = e.touches[0];
            const x = touch.pageX - carousel.offsetLeft;
            const walkX = (x - startX) * 1.5;
            carousel.scrollLeft = scrollLeft - walkX;
        }
    }, { passive: true });

    carousel.addEventListener('wheel', (e) => {
        if (window.innerWidth > 768) {
            if (e.deltaY !== 0) {
                e.preventDefault();
                carousel.scrollLeft += e.deltaY;
            }
        }
    }, { passive: false });

    // 绑定左右箭头按钮逻辑
    const prevBtn = document.getElementById('prevBtn');
    const nextBtn = document.getElementById('nextCarouselBtn');

    if (prevBtn && nextBtn) {
        const scrollAmount = 380; // 卡片宽度350 + 间距30
        prevBtn.addEventListener('click', () => {
            carousel.scrollBy({ left: -scrollAmount, behavior: 'smooth' });
        });
        nextBtn.addEventListener('click', () => {
            carousel.scrollBy({ left: scrollAmount, behavior: 'smooth' });
        });
    }
}

function splitTextToSpans(element) {
    const text = element.innerText;
    element.innerHTML = '';
    [...text].forEach((char, i) => {
        const span = document.createElement('span');
        span.innerText = char === ' ' ? '\u00A0' : char;
        span.style.display = 'inline-block';
        span.style.animationDelay = `${Math.random() * 0.5 + i * 0.05}s`;

        // 赋予随机碎片初始方向
        const rx = (Math.random() - 0.5) * 40;
        const ry = (Math.random() - 0.5) * 40;
        span.style.setProperty('--rx', `${rx}px`);
        span.style.setProperty('--ry', `${ry}px`);

        span.className = 'pixel-fragment';
        element.appendChild(span);
    });
}

