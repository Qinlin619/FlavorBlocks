function triggerWinEffects() {
    if (typeof audio !== 'undefined') audio.playWin(); // 出餐小音效

    const previewCanvas = document.getElementById('win-preview-canvas');
    const pCtx = previewCanvas.getContext('2d');

    // 生成一些礼花粒子
    const particles = [];
    const colors = ['#f44336', '#ffeb3b', '#4caf50', '#2196f3', '#9c27b0', '#ff9800'];
    const pCount = 50;

    for (let i = 0; i < pCount; i++) {
        // 让粒子从中心往四周爆炸
        const angle = Math.random() * Math.PI * 2;
        const speed = Math.random() * 6 + 2;
        particles.push({
            x: 100, // 画布200x200的中心
            y: 100,
            vx: Math.cos(angle) * speed,
            vy: Math.sin(angle) * speed,
            radius: Math.random() * 4 + 1.5,
            color: colors[Math.floor(Math.random() * colors.length)],
            alpha: 1
        });
    }

    let frame = 0;
    function animate() {
        if (frame > 125) {
            drawWinPreview(); // 在最后一帧彻底清空画布上的残影
            return;
        }
        frame++;

        // 先画静态居中的物品图片
        drawWinPreview();

        // 然后在上面叠加礼花粒子
        particles.forEach(p => {
            if (p.alpha <= 0) return; // 透明度已经是0的粒子没必要计算或绘制

            p.x += p.vx;
            p.y += p.vy;
            p.vy += 0.08; // 减轻重力，让粒子飘得更久
            p.alpha -= 0.008; // 减慢淡出速度，增加动画持续时间

            if (p.alpha < 0) p.alpha = 0;

            if (p.alpha > 0) {
                pCtx.save();
                pCtx.globalAlpha = p.alpha;
                pCtx.fillStyle = p.color;
                pCtx.beginPath();
                pCtx.arc(p.x, p.y, p.radius, 0, Math.PI * 2);
                pCtx.fill();
                pCtx.restore();
            }
        });

        if (frame <= 120) {
            requestAnimationFrame(animate);
        }
    }
    animate();
}

function drawWinPreview() {
    const previewCanvas = document.getElementById('win-preview-canvas');
    const pCtx = previewCanvas.getContext('2d');
    const mask = currentLevelData._activeMask || currentLevelData.mask;
    const rows = mask.length;
    const cols = mask[0].length;

    const previewSize = 200;
    const padding = 25; // Slightly more padding for aesthetics

    // 找到非零像素的实际边界
    let minX = cols, maxX = -1, minY = rows, maxY = -1;
    for (let y = 0; y < rows; y++) {
        for (let x = 0; x < cols; x++) {
            if (mask[y][x] !== 0) {
                if (x < minX) minX = x;
                if (x > maxX) maxX = x;
                if (y < minY) minY = y;
                if (y > maxY) maxY = y;
            }
        }
    }

    // 如果是空关卡则直接返回
    if (maxX === -1) return;

    const contentWidth = maxX - minX + 1;
    const contentHeight = maxY - minY + 1;

    // 根据实际内容大小计算格子尺寸，确保内容居中且撑满
    const cellSize = Math.min((previewSize - padding * 2) / contentWidth, (previewSize - padding * 2) / contentHeight);

    pCtx.clearRect(0, 0, previewSize, previewSize);

    // 计算居中偏移量
    const offsetX = (previewSize - contentWidth * cellSize) / 2;
    const offsetY = (previewSize - contentHeight * cellSize) / 2;

    for (let y = minY; y <= maxY; y++) {
        for (let x = minX; x <= maxX; x++) {
            const colorId = mask[y][x];
            if (colorId !== 0) {
                pCtx.fillStyle = currentLevelData.colors[colorId] || "#000";

                const px = Math.floor(offsetX + (x - minX) * cellSize);
                const py = Math.floor(offsetY + (y - minY) * cellSize);
                const nextPx = Math.floor(offsetX + (x - minX + 1) * cellSize);
                const nextPy = Math.floor(offsetY + (y - minY + 1) * cellSize);

                pCtx.fillRect(px, py, nextPx - px, nextPy - py);
            }
        }
    }
}

let isVictoryTriggered = false;

function render() {
    ctx.clearRect(0, 0, canvas.width, canvas.height);

    // 1. 绘制目标底槽
    pieces.forEach(p => p.drawTarget(ctx));

    // 1.5. 绘制提示层
    if (currentHintAlpha > 0) {
        ctx.save();
        ctx.globalAlpha = currentHintAlpha;
        pieces.forEach(p => {
            ctx.fillStyle = currentLevelData.colors[p.colorId] || "#000";
            p.pixels.forEach(pixel => {
                ctx.fillRect(
                    p.targetX + pixel.x * PIXEL_SIZE,
                    p.targetY + pixel.y * PIXEL_SIZE,
                    PIXEL_SIZE - 1,
                    PIXEL_SIZE - 1
                );
            });
        });
        ctx.restore();
    }

    // 2. 绘制已锁定的碎片（作为背景底层）
    pieces.filter(p => p.isLocked).forEach(p => p.draw(ctx));

    // 3. 绘制未锁定的碎片（悬浮在顶层，确保容易被看到和拾取）
    pieces.filter(p => !p.isLocked).forEach(p => p.draw(ctx));

    const lockedCount = pieces.filter(p => p.isLocked).length;
    const progress = Math.round((lockedCount / pieces.length) * 100);
    progressEl.innerText = `${progress}%`;

    if (lockedCount === pieces.length && pieces.length > 0 && !isVictoryTriggered) {
        isVictoryTriggered = true;
        const key = `${currentRegion}_${currentLevelIdx}`;
        if (!completedLevels[key]) {
            completedLevels[key] = true;
            localStorage.setItem('pixel_restaurant_completed', JSON.stringify(completedLevels));
            applyLanguage(); // 更新菜单上的印章
        }
        setTimeout(() => {
            triggerWinEffects();
            document.getElementById('win-title').innerText = currentLevelData.name[currentLang];
            winOverlay.classList.remove('hidden');
        }, 400);
    }
}

